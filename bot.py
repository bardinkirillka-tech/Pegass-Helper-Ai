def auto_crop_image(image_bytes):
    """Автоматически обрезает белые/пустые поля по краям изображения"""
    try:
        img = Image.open(io.BytesIO(image_bytes))
        
        if img.mode != 'RGB':
            img = img.convert('RGB')
        
        bbox = img.getbbox()
        
        if bbox:
            img_cropped = img.crop(bbox)
        else:
            img_cropped = img
        
        output = io.BytesIO()
        img_cropped.save(output, format='JPEG', quality=95)
        output.seek(0)
        return output.getvalue()
    except Exception as e:
        print(f"Ошибка обрезки: {e}")
        return image_bytes

def get_ai_response(prompt):
    try:
        completion = client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=[{"role": "user", "content": prompt}],
            temperature=0.7,
            max_tokens=500
        )
        return completion.choices[0].message.content
    except Exception as e:
        return f"❌ Ошибка ИИ: {str(e)}"

def reply_in_topic(message, text, parse_mode='Markdown'):
    """Отправляет сообщение в тот же топик (тему), откуда пришёл запрос."""
    try:
        bot.send_message(
            chat_id=message.chat.id,
            text=text,
            message_thread_id=message.message_thread_id,
            parse_mode=parse_mode
        )
    except Exception as e:
        print(f"Ошибка отправки: {e}")

